def find_gdal_library():
    possible_paths = [
        "/usr/lib/libgdal.so",
        "/usr/local/lib/libgdal.so",
        "/usr/lib/x86_64-linux-gnu/libgdal.so"
    ]
    for path in possible_paths:
        if os.path.exists(path):
            return path
    return None

GDAL_LIBRARY_PATH = os.getenv("GDAL_LIBRARY_PATH", find_gdal_library())

